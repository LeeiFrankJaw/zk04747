public class P5 {
    public static void main(String[] args) {
        int a = 0;
        System.out.print(a++ + "\t");
        System.out.print(a + "\t");
        System.out.print(++a + "\t");
        System.out.print(a + "\t");
        System.out.print(a-- + "\t");
        System.out.print(a + "\t");
        System.out.print(--a + "\t");
        System.out.println(a);
    }
}